package model.bean;

public class Pessoas {

    /**
     * Criação dos atributos da classe Pessoas
     *
     * @return
     */
    private String nomePessoas;
    private String sobrenomePessoas;

    public String getNomePessoas() {
        return nomePessoas;
    }

    public void setNomePessoas(String nomePessoas) {
        this.nomePessoas = nomePessoas;
    }

    public String getSobrenomePessoas() {
        return sobrenomePessoas;
    }

    public void setSobrenomePessoas(String sobrenomePessoas) {
        this.sobrenomePessoas = sobrenomePessoas;
    }

}
